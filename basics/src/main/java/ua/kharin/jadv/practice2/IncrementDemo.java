package ua.kharin.jadv.practice2;

public class IncrementDemo {
    public static void main(String[] args) {
        int number = 15;
        System.out.println(number);
        number = number + 1;
        System.out.println(number);
        number += 1;
        System.out.println(number++);
        System.out.println(++number);
        System.out.println(number);
    }
}
